comparativePexelInWidth(width) {
  return width / 344;
}

comparativePexelInHeight(height) {
  return height / 703;
}

class CapitalLetterPlayground extends StatefulWidget {
  const CapitalLetterPlayground({Key? key}) : super(key: key);

  @override
  State<CapitalLetterPlayground> createState() =>
      _CapitalLetterPlaygroundState();
}

class _CapitalLetterPlaygroundState extends State<CapitalLetterPlayground> {
  // final player = AudioPlayer();
  bool animate = false;
  Widget nextLetter = SizedBox();
  late CapitalLetter selectedLetter;
  late double height;
  late double width;
  @override
  void initState() {
    selectedLetter = capitalLetters[0];
    super.initState();
  }

  @override
  void dispose() {
    // player.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    return SafeArea(
      child: Scaffold(
        backgroundColor: Color.fromARGB(255, 252, 244, 243),
        appBar: AppBar(
          iconTheme: IconThemeData(
            color: HexColor('#A092DB'),
          ),
          leading: IconButton(
            onPressed: () {
              Get.back();
            },
            icon: Icon(
              Icons.arrow_back,
            ),
          ),
          backgroundColor: Colors.transparent,
          elevation: 0,
        ),
        extendBodyBehindAppBar: true,
        body: LayoutBuilder(builder: (context, constraints) {
          width = constraints.maxWidth;
          height = constraints.maxHeight;

          const distance = 51;
          // Future.delayed(Duration(milliseconds: 100), () {
          //   if (mounted) {
          //     runnerUpword(positions);
          //     setState(() {});
          //   }
          // });

          return Stack(
            children: [
              GestureDetector(
                onVerticalDragUpdate: (a) {
                  runnerUpword(positions);

                  setState(() {});
                },
                child: Stack(
                  children: capitalLetters.map((letter) {
                    return Positioned(
                      left: comparativePexelInWidth(width) *
                          positions[letter.index * distance].dx,
                      top: comparativePexelInHeight(height) *
                          positions[letter.index * distance].dy,
                      child: InkWell(
                          onTap: () {
                            // player.setAsset(letter.audioPath);
                            // player.play();
                            print("playing ${letter.letterPath}");
                            selectedLetter = capitalLetters[letter.index];
                            animate = true;
                            Future.delayed(Duration(milliseconds: 100), () {
                              animate = false;
                            });
                            setState(() {});
                          },
                          child: SvgPicture.asset(
                            letter.letterPath,
                            height: comparativePexelInWidth(width) * 30,
                            width: comparativePexelInWidth(width) * 30,
                          )),
                    );
                  }).toList(),
                ),
              ),
              Positioned(
                top: 50,
                left: 5,
                child: ClipRRect(
                  borderRadius: BorderRadius.circular(15),
                  child: Card(
                    child: SizedBox(
                      height: comparativePexelInHeight(height) * 640,
                      width: comparativePexelInWidth(width) * 225,
                      child: child(selectedLetter),
                    ),
                    color: Colors.white,
                    shadowColor: Colors.grey,
                  ),
                ),
              ),
            ],
          );
        }),
      ),
    );
  }

  child(CapitalLetter letter) {
    return Column(
      children: [
        SizedBox(
          height: 10,
        ),
        CapitalLetterAnimation(
          offsetBegin: Offset(-1, 0),
          animate: animate,
          child: SvgPicture.asset(
            letter.letterPath,
            height: comparativePexelInWidth(height) * 60,
          ),
        ),
        ...letter.examples.map((example) {
          return CapitalLetterAnimation(
            offsetBegin: Offset(-1, 0),
            index: example.index + 1,
            animate: animate,
            child: Row(
              children: [
                SizedBox(
                  height: 10,
                ),
                Image.asset(
                  example.imagePath,
                  width: comparativePexelInWidth(width) * 60,
                ),
                SizedBox(
                  width: comparativePexelInWidth(width) * 10,
                ),
                Text(
                  " = ",
                  style: TextStyle(
                    fontSize: 20,
                    color: Colors.red,
                    fontFamily: "Knicknack",
                  ),
                ),
                Text(
                  example.name,
                  style: TextStyle(
                    fontSize: 30,
                    color: Colors.red,
                    fontFamily: "Knicknack",
                  ),
                ),
              ],
            ),
          );
        }).toList(),
        FutureBuilder<Widget>(
            future: Future.delayed(
                Duration(
                  seconds: 5,
                ), () {
              return BounceAnimaiton(
                child: InkWell(
                  onTap: () {
                    selectedLetter = capitalLetters[letter.index + 1];
                    // player.setAsset(selectedLetter.audioPath);
                    // player.play();
                    print("playing ${selectedLetter.letterPath}");
                    animate = true;
                    Future.delayed(Duration(milliseconds: 100), () {
                      animate = false;
                    });
                    setState(() {});
                  },
                  child: SvgPicture.asset(
                    capitalLetters[letter.index + 1].letterPath,
                    height: comparativePexelInWidth(height) * 70,
                    width: comparativePexelInWidth(height) * 70,
                  ),
                ),
              );
            }),
            builder: (context, snapshot) {
              return snapshot.data ?? SizedBox();
            }),
      ],
    );
  }
}

